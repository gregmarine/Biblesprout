#!/usr/bin/env python3
"""Build the enriched read-only `bsb.bible` SQLite source from official BSB USFM.

Source of truth: the per-book USFM in `data/bible/bsb_usfm/` (66 files, one per
book), downloaded from https://bereanbible.com/bsb_usfm.zip (public domain, BSB
3rd printing, generated by the BSB-publishing/bsb2usfm toolchain). To refresh,
re-download the zip, unzip over `data/bible/bsb_usfm/`, and re-run this script.

Run from the repo root (needs Python 3 with an FTS5-capable sqlite3 — the stock
macOS build has it):

    python3 data/tools/build_bible_db.py

Output: `data/bible/bsb.bible`, which the Android app's Gradle build copies into
its content assets (see apps/biblesprout_android/app/build.gradle.kts).

Schema (v2): the original plain-text layer is preserved verbatim so existing
consumers keep working —

    metadata, book, verse (clean plain text), verse_fts (FTS5 over verse.text)

— and a rich display layer is added alongside, addressed by character offsets so
Android's StaticLayout can turn them straight into spans:

    block         one row per rendered block (paragraph / poetry line / heading /
                  stanza break), in document order (id == reading order). `content`
                  is the display text; `kind` is the USFM paragraph/heading marker.
    verse_marker  superscript verse-number spans within a block's content.
    redletter     words-of-Jesus (\\wj) spans within a block's content.
    footnote      a footnote: caller offset in a block + body display text.
    xref          a cross-reference link span (in a block or a footnote body) with
                  its resolved target verse-key range — tap to navigate.

`verse.text` (clean, markup-free) feeds FTS, so search never trips on poetry
markers, footnote text, heading text or cross-reference labels.
"""

